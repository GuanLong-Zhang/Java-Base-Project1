//变量案例
public class Virable {
    public static void main(String[] args){
        int a = 10;
        System.out.println(a);
        a = 20;
        System.out.println(a);
        long b = 10000000000L;    //默认为int,所以定义的时候需要加上L
        System.out.println(b);
        float c = 3.14F;          //默认为double,所以定义的时候需要加上F
        System.out.println(c);
    }
}
